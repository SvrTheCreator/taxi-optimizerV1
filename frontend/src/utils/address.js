// Сокращение адреса ТОЛЬКО для отображения: убираем административные части
// (город Ростов-на-Дону, область, страна), оставляя улицу и дом. В БД и в
// геокодинг адрес уходит полным — это чисто косметика на экране.
// Города-исключения (Батайск, Азов и т.п.) не в списке, поэтому сохраняются.
const DROP_PARTS = [
  'россия',
  'ростовская обл',
  'ростовская область',
  'ростов-на-дону',
  'г ростов-на-дону',
  'г. ростов-на-дону',
  'город ростов-на-дону',
]

// Работает и для отдельного адреса, и для строки уведомления, внутри которой
// адрес записан через запятую (лишних запятых в остальном тексте нет).
export function shortAddr(text) {
  if (!text || typeof text !== 'string') return text
  return text
    .split(',')
    .map(p => p.trim())
    .filter(p => p && !DROP_PARTS.includes(p.toLowerCase().replace(/\.$/, '')))
    .join(', ')
}
